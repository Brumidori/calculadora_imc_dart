class Pessoa {
  String _nome;
  double peso;
  double altura;

  Pessoa(this._nome, this.peso, this.altura);

  void setNome(String nome) {
    _nome = _nome;
  }

  String getNome() {
    return _nome;
  }

  void setAltura(double altura) {
    altura = altura;
  }

  double getAltura() {
    return altura;
  }

  void setPeso(double peso) {
    peso = peso;
  }

  double getPeso() {
    return peso;
  }
}
